-- Trigger kiểm tra tính hợp lệ của DOI_TAC
CREATE TRIGGER TRG_DOITAC_CHECK_KHACHHANG
BEFORE INSERT ON DOI_TAC FOR EACH ROW
BEGIN
    IF NEW.LOAIDOITAC IN ('KHACH_HANG', 'CA_HAI') THEN
        IF NEW.TENDOITAC IS NULL OR TRIM(NEW.TENDOITAC) = '' THEN SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Tên không được rỗng'; END IF;
        IF NEW.SODIENTHOAI IS NULL OR NOT (NEW.SODIENTHOAI REGEXP '^[0-9]{10,11}$') THEN SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'SĐT không hợp lệ'; END IF;
    END IF;
END $$

-- Trigger tự động nâng hạng khi thay đổi điểm (Áp dụng cả khi Admin sửa điểm)
CREATE TRIGGER TRG_CAPNHAT_LOAI_KHACH
BEFORE UPDATE ON KHACH_HANG FOR EACH ROW
BEGIN
    IF NEW.DIEMTICHLUY < 1000 THEN SET NEW.LOAIKHACHHANG = 'Đồng';
    ELSEIF NEW.DIEMTICHLUY < 5000 THEN SET NEW.LOAIKHACHHANG = 'Bạc';
    ELSE SET NEW.LOAIKHACHHANG = 'Vàng';
    END IF;
END $$

-- Trigger tự động set hạng khi Insert
CREATE TRIGGER TRG_KHACHHANG_RANK_INSERT
BEFORE INSERT ON KHACH_HANG FOR EACH ROW
BEGIN
    IF NEW.DIEMTICHLUY < 1000 THEN SET NEW.LOAIKHACHHANG = 'Đồng';
    ELSEIF NEW.DIEMTICHLUY < 5000 THEN SET NEW.LOAIKHACHHANG = 'Bạc';
    ELSE SET NEW.LOAIKHACHHANG = 'Vàng';
    END IF;
END $$

-- Trigger tích điểm khi có hóa đơn
CREATE TRIGGER TRG_HOADON_TICH_DIEM
AFTER INSERT ON HOA_DON_BAN_HANG FOR EACH ROW
BEGIN
    UPDATE KHACH_HANG SET DIEMTICHLUY = DIEMTICHLUY + NEW.DIEMTICHDUOC - NEW.DIEMSUDUNG WHERE MADOITAC = NEW.MADOITAC;
END $$

