CREATE FUNCTION FN_KHACH_HANG_TONTAI(p_MADOITAC INT)
RETURNS BOOLEAN DETERMINISTIC
BEGIN
    RETURN EXISTS(SELECT 1 FROM KHACH_HANG WHERE MADOITAC = p_MADOITAC);
END $$

CREATE FUNCTION FN_SDT_DOI_TAC_TRUNG(p_SODIENTHOAI NVARCHAR(255), p_MADOITAC INT)
RETURNS BOOLEAN DETERMINISTIC
BEGIN
    RETURN EXISTS(SELECT 1 FROM DOI_TAC WHERE SODIENTHOAI = p_SODIENTHOAI AND (p_MADOITAC IS NULL OR MADOITAC <> p_MADOITAC));
END $$

CREATE FUNCTION FN_EMAIL_DOI_TAC_TRUNG(p_EMAIL NVARCHAR(255), p_MADOITAC INT)
RETURNS BOOLEAN DETERMINISTIC
BEGIN
    IF p_EMAIL IS NULL OR TRIM(p_EMAIL) = '' THEN RETURN FALSE; END IF;
    RETURN EXISTS(SELECT 1 FROM DOI_TAC WHERE EMAIL = p_EMAIL AND (p_MADOITAC IS NULL OR MADOITAC <> p_MADOITAC));
END $$

